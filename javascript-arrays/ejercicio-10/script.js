// Escribir una función que tome una matriz de cadenas como argumento
// Ordenar alfabéticamente los elementos de la matriz
// Devuelve el resultado
function ordenarAlfabeticamenteLosElementosDeUnaMatriz(arr) {
  return arr.sort();
}
console.log(
  ordenarAlfabeticamenteLosElementosDeUnaMatriz(["b", "c", "d", "a"])
);
console.log(
  ordenarAlfabeticamenteLosElementosDeUnaMatriz([
    "z",
    "c",
    "d",
    "a",
    "y",
    "a",
    "w",
  ])
);
